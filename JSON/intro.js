/*
1. what is JSON ?
  - JSON stand for JavaScript Object Notation
  - JSON is a format for storing and transporting data
  - JSON is often used when data is sent from a server to web page
  - JSON is lightweight data interchange format 
  - JSON is language indenpent
  - JSON is "self-describing" and easy to undersatnd
     
    JSON is not a language . its using for transmitting the data . and it working with all type of programming language
*/

/* 
2. JSON data type
  in JSON must be one of the following data type

  - a string
  - a number
  - an object (JSON object)
  - an array
  - a boolean 
  - null
*/

/* 
3. JSON data type (not supported by JSON)
  following data type JSON cannot supported

  - a function 
  - a date
  - undefined

*/

/* 
4. JSON syntax rules

  - data is in name / value pairs
  - data is separated by commas
  - JSON object are surrounded by curly bracess {}
  - curly bracess hold object
  - square brackets hold array
*/

